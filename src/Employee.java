public class Employee {
    private String name;
    private int ID;
    private int salary;



//    GETTERS AND SETTERS
    public String getName(){
        return name;
    }

    void setName(String name){
        this.name = name;
    }

    public  int  getID(){
        return  ID;
    }

    void setID(int ID){
        this.ID = ID;
    }

    public  int getSalary(){
        return  salary;
    }

    void setSalary(int salary){
        this.salary = salary;
    }





}
