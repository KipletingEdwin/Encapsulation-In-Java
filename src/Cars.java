public class Cars {
    private String make;
    private int year;
    private  String type;

    public Cars(String make, int year, String type) {
        this.make = make;
        this.year = year;
        this.type = type;
    }

    public String getMake() {
        return make;
    }

    public void setMake(String make) {
        this.make = make;
    }

    public int getYear() {
        return year;
    }

    public void setYear(int year) {
        this.year = year;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }
}
